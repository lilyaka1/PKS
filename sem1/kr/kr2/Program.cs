using System;

/// Калькулятор для работы с матрицами
/// Позволяет вводить матрицы, вычислять обратные матрицы

class КалькуляторМатрицы
{
    static void Main()
    {
        Console.WriteLine("Калькулятор матриц");

        // Ввод размера матрицы
        Console.WriteLine("Введите количество строк n:");
        int n = int.Parse(Console.ReadLine());

        Console.WriteLine("Введите количество столбцов m:");
        int m = int.Parse(Console.ReadLine());

        // Создание двух матриц заданного размера
        double[,] матрица1 = new double[n, m];
        double[,] матрица2 = new double[n, m];

        // Выбор способа заполнения матриц (клавиатура или случайные числа)
        Console.WriteLine("Выберите способ ввода:\n1 - ввод с клавиатуры\n2 - генерация случайных чисел");
        int выбор = int.Parse(Console.ReadLine());

        if (выбор == 1)
        {
            // Ввод первой матрицы с клавиатуры
            Console.WriteLine("Заполните первую матрицу (ввод с клавиатуры):");
            ВводМатрицы(матрица1);

            Console.WriteLine("Первая матрица:");
            ВыводМатрицы(матрица1);

            // Ввод второй матрицы с клавиатуры
            Console.WriteLine("Заполните вторую матрицу (ввод с клавиатуры):");
            ВводМатрицы(матрица2);

            Console.WriteLine("Вторая матрица:");
            ВыводМатрицы(матрица2);
        }
        else if (выбор == 2)
        {
            // Заполнение случайными числами
            Console.WriteLine("Введите минимальное значение случайного числа a:");
            int a = int.Parse(Console.ReadLine());

            Console.WriteLine("Введите максимальное значение случайного числа b:");
            int b = int.Parse(Console.ReadLine());

            Random rnd = new Random();

            ЗаполнениеСлучайнымиЧислами(матрица1, a, b, rnd);
            Console.WriteLine("Первая матрица:");
            ВыводМатрицы(матрица1);

            ЗаполнениеСлучайнымиЧислами(матрица2, a, b, rnd);
            Console.WriteLine("Вторая матрица:");
            ВыводМатрицы(матрица2);
        }
        else
        {
            Console.WriteLine("Неверный выбор способа ввода.");
            return;
        }

        // Вычисление обратной матрицы для квадратных матриц
        // Обратная матрица существует только для квадратных матриц с ненулевым определителем
        if (n == m)
        {
            try
            {
                Console.WriteLine("Обратная матрица первой матрицы:");
                double[,] inv1 = ОбратнаяМатрица(матрица1);
                ВыводМатрицы(inv1);
            }
            catch (ArgumentException ex)
            {
                Console.WriteLine($"Ошибка при вычислении обратной матрицы первой матрицы: {ex.Message}");
            }

            try
            {
                Console.WriteLine("Обратная матрица второй матрицы:");
                double[,] inv2 = ОбратнаяМатрица(матрица2);
                ВыводМатрицы(inv2);
            }
            catch (ArgumentException ex)
            {
                Console.WriteLine($"Ошибка при вычислении обратной матрицы второй матрицы: {ex.Message}");
            }
        }
        else
        {
            Console.WriteLine("Вычисление обратной матрицы возможно только для квадратных матриц.");
        }
    }

    /// Ввод элементов матрицы с клавиатуры

    static void ВводМатрицы(double[,] матрица)
    {
        int n = матрица.GetLength(0);
        int m = матрица.GetLength(1);
        for (int i = 0; i < n; i++)
        {
            Console.WriteLine($"Введите элементы {i + 1}-й строки через пробел:");
            string[] части = Console.ReadLine().Split(' ', StringSplitOptions.RemoveEmptyEntries);
            if (части.Length != m)
            {
                Console.WriteLine("Неверное количество элементов. Попробуйте ещё раз.");
                i--;
                continue;
            }
            bool строкаВерна = true;
            for (int j = 0; j < m; j++)
            {
                if (double.TryParse(части[j], out double значение))
                {
                    матрица[i, j] = значение;
                }
                else
                {
                    Console.WriteLine("Ошибка ввода. Повторите ввод всей строки.");
                    строкаВерна = false;
                    break;
                }
            }
            if (!строкаВерна) i--;
        }
    }


    /// Заполнение матрицы случайными числами в заданном диапазоне

    static void ЗаполнениеСлучайнымиЧислами(double[,] матрица, int a, int b, Random rnd)
    {
        int n = матрица.GetLength(0);
        int m = матрица.GetLength(1);
        for (int i = 0; i < n; i++)
            for (int j = 0; j < m; j++)
                матрица[i, j] = rnd.Next(a, b + 1);
    }

    /// Вывод матрицы на экран в табличном формате

    static void ВыводМатрицы(double[,] матрица)
    {
        int n = матрица.GetLength(0);
        int m = матрица.GetLength(1);
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < m; j++)
                Console.Write($"{матрица[i, j]:F2}\t");
            Console.WriteLine();
        }
    }


    /// Рекурсивное вычисление определителя матрицы
    /// Использует разложение по первой строке
    /// Подходит для небольших матриц (n <= 10)

    static double Определитель(double[,] матрица)
    {
        int n = матрица.GetLength(0);
        int m = матрица.GetLength(1);
        if (n != m) throw new ArgumentException("Матрица должна быть квадратной для вычисления определителя.");

        // Базовый случай: матрица 1x1
        if (n == 1)
            return матрица[0, 0];

        // Базовый случай: матрица 2x2
        if (n == 2)
            return матрица[0, 0] * матрица[1, 1] - матрица[0, 1] * матрица[1, 0];

        // Рекурсивное разложение по первой строке
        double дет = 0;
        for (int p = 0; p < n; p++)
        {
            double[,] минор = ПолучитьМинор(матрица, 0, p);
            дет += матрица[0, p] * Определитель(минор) * ((p % 2 == 0) ? 1 : -1);
        }
        return дет;
    }


    /// Получение минора матрицы (матрица без указанной строки и столбца)
    static double[,] ПолучитьМинор(double[,] матрица, int строка, int столбец)
    {
        int n = матрица.GetLength(0);
        double[,] минор = new double[n - 1, n - 1];
        int r = 0;
        for (int i = 0; i < n; i++)
        {
            if (i == строка) continue;
            int c = 0;
            for (int j = 0; j < n; j++)
            {
                if (j == столбец) continue;
                минор[r, c] = матрица[i, j];
                c++;
            }
            r++;
        }
        return минор;
    }

    /// Нахождение обратной матрицы через матрицу кофакторов (присоединённую матрицу)
    static double[,] ОбратнаяМатрица(double[,] матрица)
    {
        // Вычисляем определитель
        double дет = Определитель(матрица);
        // Проверяем, что определитель не равен нулю (матрица обратима)
        if (Math.Abs(дет) < 1e-10) throw new ArgumentException("Обратная матрица не существует.");

        int n = матрица.GetLength(0);
        // Создаём присоединённую матрицу (матрицу кофакторов, транспонированную)
        double[,] присоединённая = new double[n, n];

        // Вычисляем элементы присоединённой матрицы
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                // Получаем минор для элемента (i, j)
                double[,] минор = ПолучитьМинор(матрица, i, j);
                // Вычисляем кофактор = (-1)^(i+j) * det(минор)
                double кофактор = Определитель(минор);
                if ((i + j) % 2 == 1) кофактор = -кофактор;
                // Транспонируем при присваивании (меняем i и j местами)
                присоединённая[j, i] = кофактор;
            }
        }

        // Делим каждый элемент присоединённой матрицы на определитель
        double[,] обратная = new double[n, n];
        for (int i = 0; i < n; i++)
            for (int j = 0; j < n; j++)
                обратная[i, j] = присоединённая[i, j] / дет;

        return обратная;
    }
}
